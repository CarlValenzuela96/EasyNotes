   private void validacionNota(String cadena, java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();

        if (!(c >= '1' && c <= '7') && c != '.') {
            evt.consume();
        }
        if ((c == '.') && cadena.contains(".")) {
            evt.consume();
        }
        if (cadena.length() >= 5) {
            evt.consume();
        }
    }
